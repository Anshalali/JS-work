// shift array it can be used to remove an element from the beginning of an array
// (manipulate arrays with shift)

var myArray = [["dogs", 5], ["ctas", 3], ["hat", 1]];
var removeFromMyArray = myArray.shift();
console.log(removeFromMyArray)

var myArray = [["dogs", 5], ["ctas", 3], ["hat", 1]];
myArray.shift();
console.log(removeFromMyArray)