// pop array it can be used to remove an element from the end of an array
// (manipulate arrays with pop)

var myArray =[["balls", 20], ["bats", 3], ["helmet", 1]];
myArray.pop(["helmet", 1]);
console.log(myArray)

var myArray =[["bikes", 20], ["cars", 3], ["bus", 1]];
myArray.pop();
console.log(myArray)