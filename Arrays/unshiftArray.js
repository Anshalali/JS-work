// unshift array it can be used to add an element to the beginning of an array
// (manipulate arrays with unshift)

var myArray = [["balls", 6], ["bats", 3]];
myArray.unshift(["helmets", 2]);
console.log(myArray)

var myArray = [["balls", 6], ["bats", 3], ["helmets", 2]];
myArray.shift();
myArray.unshift(["hat", 1]);
console.log(myArray)


